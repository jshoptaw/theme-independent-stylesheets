<blockquote cite="http://www.w3.org/TR/CSS2/media.html#media-types">
    <dl>
        <dt><strong>all</strong></dt>

        <dd>Suitable for all devices.</dd>

        <dt><strong>braille</strong></dt>

        <dd>Intended for braille tactile feedback devices.</dd>

        <dt><strong>embossed</strong></dt>

        <dd>Intended for paged braille printers.</dd>

        <dt><strong>handheld</strong></dt>

        <dd>Intended for handheld devices (typically small screen, limited bandwidth).</dd>

        <dt><strong>print</strong> </dt>

        <dd>Intended for paged material and for documents viewed on screen in print preview mode.  Please consult the section on <a href="http://www.w3.org/TR/CSS2/page.html">paged media</a> for information about formatting issues that are specific to paged media.</dd>

        <dt><strong>projection</strong></dt>

        <dd>Intended for projected presentations, for example projectors. Please consult the section on <a href="http://www.w3.org/TR/CSS2/page.html">paged media</a> for information about formatting issues that are specific to paged media.</dd>

        <dt><strong>screen</strong></dt>

        <dd>Intended primarily for color computer screens. </dd>

        <dt><strong>speech</strong></dt>

        <dd>Intended for speech synthesizers. Note: CSS2 had a similar media type called \'aural\' for this purpose. See the appendix on <a href="http://www.w3.org/TR/CSS2/aural.html">aural style sheets</a> for details.</dd>

        <dt><strong>tty</strong></dt>

        <dd>Intended for media using a fixed-pitch character grid (such as teletypes, terminals, or portable devices with limited display capabilities). Authors should not use <a href="http://www.w3.org/TR/CSS2/syndata.html#length-units">pixel units</a> with the "tty" media type.</dd>

        <dt><strong>tv</strong></dt>

        <dd>Intended for television-type devices (low resolution, color, limited-scrollability screens, sound available).</dd>
    </dl>
